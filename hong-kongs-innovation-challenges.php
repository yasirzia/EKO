                                                    <p>There&rsquo;s no easy way to say this so we&rsquo;re just going to rip off the bandaid&hellip;</p>
<p><strong>Startups perceive Hong Kong (HK) to be lagging behind in technology, activity, and talent (especially in relation to Shenzhen).</strong></p>
<p>Ouch.</p>
<p><a href="http://blog.nest.vc/why-startups-love-hong-kong/">Our last blog post </a>explored why startups love HK. On the flip side, we wanted to understand the challenges entrepreneurs faced and their thoughts on the current state of innovation in the region. Here are some of our findings:</p>
<p>One of the first challenges we must face is attitude. The mindset and culture of HK needs to improve in order to embrace innovation. Some founders believe that there needs to be a mindset shift in order for employees and companies to become more creative and innovative.</p>
<p><strong>Some common startup perceptions</strong>:</p>
<p>The workforce is perceived to be resistant to change overall and requires a lot of direction to drive <a href="http://innovation.hk/">innovation.HK</a> is lacking creativity and innovative thinking. Startups in HK have a very different dynamic to the startups in Shenzhen, Guangzhou or <a href="http://shanghai.hk/">Shanghai.HK</a> is considered as a finance hub while Shenzhen is seen as the tech and innovation hub. Mainland China is more tech savvy than HK.Tech talent is an issue in HK as it is increasingly going to Shenzhen.</p>
<p><em>&ldquo;<strong>The outlook of many people is very transactional and very opportunistic.</strong></em> <strong><em>Changing their ways is not an easy thing to do because of the legacy systems in place</em></strong><em>.&rdquo; &ndash; Anonymous Startup</em></p>
<p>Some startups feel that HK has always been known as a financial hub and it will be difficult for people to see HK as a tech hub when we lack talent (people are going to mainland China instead) and innovation, especially when Shenzhen is the tech powerhouse in many startups&rsquo; minds.</p>
<p>With these innovation challenges, you might think startups have given up on HK but the opposite is actually true. Startups are still very optimistic about the future. Why?</p>
<p>Startups are energised at finding new solutions to address the lack of tech and innovation in the region, often partnering with corporates to bring about change that will ultimately benefit the consumer.</p>
<p>Overall, startups feel that HK can still catch up and become more innovative if we take immediate steps to address the challenges and prioritise goals.</p>
