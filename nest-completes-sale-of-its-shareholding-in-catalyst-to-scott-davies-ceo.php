                                                  <p><strong>PRESS RELEASE: Nest Completes Sale of its Shareholding in Catalyst to Scott Davies, CEO.</strong></p>
<p><strong>Hong Kong, 24 June 2019 &ndash; </strong>Nest Ventures, the investment arm of Hong Kong headquartered corporate innovation group Nest, and Catalyst Agency (formerly iLove) are pleased to announce that Nest has sold its shareholding in the company to Scott Davies, Chief Executive Officer of Catalyst.</p>
<p>Nest management co-founded Catalyst in 2011 and over the years, under Scott Davies&rsquo; leadership, it has become a leading integrated brand experience agency in Asia Pacific. Catalyst&rsquo;s projects span the region and its client portfolio includes top international brands and Fortune 500 companies.</p>
<p>&ldquo;We are proud to see Catalyst and its client base grow on an international scale. With the completion of the buy-out, Catalyst will invest in growth to satisfy the needs of our clients, starting by solidifying our presence in Sydney, followed by the opening of Shanghai and Shenzhen operations&rdquo; said Scott Davies.</p>
<p>Dennis Plomp, Principal at Nest Ventures, said &ldquo;This transaction marks a new chapter for Scott and Catalyst and we&rsquo;re proud to be one of the early investors in the company. We have witnessed the success and business traction that Catalyst has achieved over the years and we are excited to see the company continue to grow following our exit. "</p>
<p>"We are looking forward to the new and exciting ventures which are in the works over the coming years and wish Nest continued success with their investments and programmes&rdquo; said Scott Davies.</p>
<p>----END----</p>
<p><strong>About Nest </strong></p>
<p>Nest operates corporate innovation accelerators, invests in early-stage startups, and runs an entrepreneurial community called Metta. Nest has built an innovation ecosystem to connect the fast-growing markets of Asia, Middle East and Africa.</p>
<p>Global brands including INFINITI, Bangkok Bank, DBS, FedEx and American Express, as well as various governments, trust Nest to source and facilitate collaboration with leading startups that solve real business challenges and deliver tangible commercial outcomes. Nest is headquartered in Hong Kong and has regional offices in Bangkok, Bahrain and Nairobi.</p>
<p><strong>About Catalyst</strong></p>
<p>Founded in Hong Kong, Catalyst is an integrated creative agency helping clients to deliver engaging O2O experiences using content, event conceptualization and management, branding, digital, social and live experiences.</p>
<p>Catalyst&rsquo;s niche is helping international brands speak to audiences in Asia Pacific in a localized way, whilst staying true to the brands international ethos. Catalyst serves clients from sectors including retail, FMCG, media, financial services and B2B.</p>
<p>Catalyst was formerly known as iLove.</p>
