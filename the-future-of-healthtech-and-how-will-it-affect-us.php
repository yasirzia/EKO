                                                 <p>HealthTech, involves the integration of technology into what still remains a fairly traditional industry. It encompasses everything from medical services that can perform more efficiently to wellness solutions that allow customers to better track, diagnose and manage their own health.</p>
<p>Consumers have been empowered by technology and choice, and in turn have become more demanding about the information they receive. With 2019 set to be another significant year in HealthTech we discussed with Ivan Li from Prenetics, how technology and changes in behaviour have disrupted the industry and how it will affect the future.</p>
<h3>How do you see the future of wellness and health(tech) evolving? What are some of the most pressing concerns you have in this space? What disruption or new tech excites you most?</strong></h3>
<p>We see wellness and health(tech) to be an increasingly important topic, and we are really excited about the continued movement and trajectory towards Preventive and Personalised Health. We see a lot of sciences and technology development supporting that movement, such as, sensors, genomics, lab-made organs, Machine learning/ AI, etc.</p>
<p>We are especially excited about the power of genomics, as a key enabler to personalised and preventive care. Through a saliva test, we can discover so many things about you, ranging from dietary needs to diseases risks; these are really powerful indicators in helping people better manage their health.</p>
<h3>Innovative virtual care offerings, like telehealth and remote patient monitoring offer major benefits to patients, do you think the industry is ready to adopt these new technologies?</strong></h3>
<p>From a technology standpoint, the ingredients are increasingly there. We do see the global industry needing to address issues around awareness, quality control, liability concerns, and how this integrates with payment models.</p>
<p>Of course, we see a lot more support in China, and its ability to really help address shortage and accessibility issues. It's also notable that these efforts are built upon established platforms from giants such as Alibaba, which help address scale and awareness issues that other systems may face. </p>
<h3>Data privacy and protecting customer information in a constant concern, especially regarding your health. Keeping information private, but also allowing the patient to be in control is a difficult balance. How big of a risk do some of these new technologies pose in keeping our data safe?</strong></h3>
<p>We recognise the risks, which is why data privacy and security is always going to be a top of mind priority for us. We take the protection of our customer&rsquo;s data and privacy very seriously, and administer a number of steps and protocols to ensure that it remains safe and secure. We adhere to international and industry standards, including ISO 27001 Information Security Standard, designed to secure our customer&rsquo;s DNA data and personal information. Our customers&rsquo; DNA data are all encrypted and anonymised, decoupled from personal identifying information, and we conduct regular information security tests by external consultants to ensure the quality of our service.</p>
<p>As Prenetics has highlighted, there is a lot of exciting health tech technology currently surfacing in the industry and once it becomes more widely available, will allow people to manage their health better.</p>
<p>Startup bio: </strong></p>
<p>Prenetics</strong> is the leading genetic testing/digital health company in Southeast Asia. They empower individuals with valuable health information. With a simple saliva sample, individuals can discover how their genes affect their diet, drug response, disease and cancer risk. This information enables people to make health a choice, by living healthier and proactively preventing diseases.</p>
